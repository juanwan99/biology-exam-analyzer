    def __init__(self):
        self._synonyms = {}
        try:
            import json, os
            syn_path = os.path.join(os.path.dirname(__file__), "knowledge_synonyms.json")
            if os.path.exists(syn_path):
                with open(syn_path, "r", encoding="utf-8") as f:
                    raw = json.load(f)
                for canonical, aliases in raw.items():
                    for alias in aliases:
                        self._synonyms[alias] = canonical
                logger.info(f"[知识点映射器] 同义词表加载: {len(self._synonyms)} 条")
        except Exception as e:
            logger.warning(f"[知识点映射器] 同义词表加载失败: {e}")
        logger.info("[知识点映射器] 初始化完成")
        logger.info(f"[知识点映射器] 教材结构：{len(self.TEXTBOOK_STRUCTURE)}本教材，关键词映射表：{len(self.KEYWORD_MAPPING)}条")

    def map_knowledge_point(self, knowledge_point: str) -> Dict[str, Any]:
        """
        映射单个知识点到教材章节

        Args:
            knowledge_point: LLM返回的知识点（如"细胞膜的流动镶嵌模型"）

        Returns:
            {
                "original": "细胞膜的流动镶嵌模型",
                "mapped": True/False,
                "textbook": "必修1",
                "textbook_name": "分子与细胞",
                "chapter": "第3章",
                "chapter_name": "细胞的基本结构",
                "section": "3.1",
                "section_name": "细胞膜的结构和功能"
            }
            或（映射失败时）
            {
                "original": "细胞膜的流动镶嵌模型",
                "mapped": False
            }
        """
        # 同义词展开：如果输入匹配同义词，追加规范名用于匹配
        expanded = [knowledge_point]
        for alias, canonical in self._synonyms.items():
            if alias in knowledge_point and canonical not in knowledge_point:
                expanded.append(knowledge_point.replace(alias, canonical))
                expanded.append(canonical)

        # 尝试关键词匹配（含同义词展开）
        for kp_variant in expanded:
            for keyword, location in self.KEYWORD_MAPPING.items():
                if keyword in kp_variant:
                    textbook, chapter, section = location

                    textbook_data = self.TEXTBOOK_STRUCTURE.get(textbook, {})
                    chapter_data = textbook_data.get("chapters", {}).get(chapter, {})
                    section_name = chapter_data.get("sections", {}).get(section, "")

                    result = {
                        "original": knowledge_point,
                        "mapped": True,
                        "textbook": textbook,
                        "textbook_name": textbook_data.get("name", ""),
                        "chapter": chapter,
                        "chapter_name": chapter_data.get("name", ""),
                        "section": section,
                        "section_name": section_name
                    }

                    logger.debug(f"[知识点映射] '{knowledge_point}' → {textbook} {chapter} {section}")
                    return result

        # Fallback: 反向匹配 — 检查关键词表中哪些关键词是输入的子串
        best_match = None
        best_len = 0
        for kp_variant in expanded:
            for keyword, location in self.KEYWORD_MAPPING.items():
                if keyword in kp_variant and len(keyword) > best_len:
                    best_match = (keyword, location)
                    best_len = len(keyword)
                elif kp_variant in keyword and len(kp_variant) > best_len:
                    best_match = (keyword, location)
                    best_len = len(kp_variant)

        # Fallback: 章节名包含匹配
        if not best_match:
            for kp_variant in expanded:
                for tb_key, tb_data in self.TEXTBOOK_STRUCTURE.items():
                    for ch_key, ch_data in tb_data.get("chapters", {}).items():
                        ch_name = ch_data.get("name", "")
                        for sec_key, sec_name in ch_data.get("sections", {}).items():
                            if len(kp_variant) >= 3:
                                if kp_variant[:3] in sec_name or sec_name[:3] in kp_variant:
                                    overlap = len(set(kp_variant) & set(sec_name))
                                    if overlap > best_len:
                                        best_match = (sec_name, (tb_key, ch_key, sec_key))
                                        best_len = overlap

        if best_match:
            keyword, location = best_match
            textbook, chapter, section = location
            textbook_data = self.TEXTBOOK_STRUCTURE.get(textbook, {})
            chapter_data = textbook_data.get("chapters", {}).get(chapter, {})
            section_name = chapter_data.get("sections", {}).get(section, "")
            logger.debug(f"[知识点映射] '{knowledge_point}' 模糊匹配 → {textbook} {chapter} ({keyword})")
            return {
                "original": knowledge_point,
                "mapped": True,
                "fuzzy": True,
                "textbook": textbook,
                "textbook_name": textbook_data.get("name", ""),
                "chapter": chapter,
                "chapter_name": chapter_data.get("name", ""),
                "section": section,
                "section_name": section_name,
            }

        logger.info(f"[知识点映射] '{knowledge_point}' 映射失败，返回原始内容")
        return {
            "original": knowledge_point,
            "mapped": False
        }

    def map_knowledge_points(self, knowledge_points: List[str]) -> List[Dict[str, Any]]:
        """
        批量映射知识点

        Args:
            knowledge_points: LLM返回的知识点列表

        Returns:
            映射结果列表
        """
        results = []
        for kp in knowledge_points:
            results.append(self.map_knowledge_point(kp))

        # 统计
        mapped_count = sum(1 for r in results if r.get("mapped"))
        logger.info(f"[知识点映射] 完成，总数={len(results)}，成功映射={mapped_count}，失败={len(results) - mapped_count}")

        return results

